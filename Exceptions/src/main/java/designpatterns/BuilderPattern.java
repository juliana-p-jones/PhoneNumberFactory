package designpatterns;

public class BuilderPattern {
    /*
    Summary: creative way to deal with complexity, it is easy to implement, few drawbacks, can refactor in with separate class

       Helps address the telescoping anti-pattern

    Pitfalls: objects are immutable
    Inner Static class needs to be worked around
    Designed first
    Complexity

    Contrasting the builder pattern with the prototype pattern:
    builder: handles complex constructors, no interface required, can be a separate class, works with legacy code
    prototype: implemented around a clone, avoids calling complex constructors, difficult to implement in legacy code

     */

}
